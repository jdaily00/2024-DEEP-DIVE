// 주석
package sec02.chap02;

public class Main {
    public static void main(String[] args) {
        System.out.println("Hello World");
        // System.out.println("주석이지롱");
        System.out.println("헬로우 월드");

        //  한 줄짜리 주석
        System.out.println("오른쪽을 봐"); // 주석이 요기잉네?

        /* 주석은 이와 같이 여러 줄에 걸쳐
        작성할 수도 있습니다.
        장문의 주석을 달거나

        System.out.println("아...!");
        System.out.println("안돼!!");

        위와 같이
        코드의 일부를 주석처리하여
        비활성화는 등의 목적으로 사용되죠. */

        System.out.println("돼.");
    }
}